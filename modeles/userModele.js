import mongoose from "./index.js";

const userSchema = new mongoose.Schema({
  firstname: {
    type: String,
    required: true
  },
  lastname: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  }
});

//const User = mongoose.model('User', userSchema);

export default mongoose.model('User', userSchema);